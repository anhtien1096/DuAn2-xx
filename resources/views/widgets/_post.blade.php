<p>Chỉ đạo về vấn đề này, lãnh đạo Bộ TT&amp;TT cho biết, Bộ TT&amp;TT quyết ra tay dẹp nạn SIM rác, tin nhắn rác. Bộ TT&amp;TT yêu cầu 3 nhà mạng lớn nhất là Viettel, MobiFone, VinaPhone phải tuyệt đối tuân thủ những cam kết thu hồi SIM trả trước kích hoạt sẵn trên kênh phân phối cũng như về giá cước, khuyến mại. Nếu công tác này bị lơ là có thể vấn nạn SIM rác, tin nhắn rác lại bùng phát trở lại.</p>

<p>Trong nghị định quản lý thuê bao trả trước đang trình Chính phủ ban hành sẽ quy định chặt chẽ về trách nhiệm của nhà mạng trong việc đăng ký thông tin thuê bao đi kèm với các chế tài xử phạt rất nặng đối với các hành vi sai phạm về quản lý thuê bao di động trả trước. Nghị định này sẽ thay thế quy định cũ về quản lý thuê bao trả trước và sẽ loại bỏ việc các đại lý được kích hoạt SIM như hiện nay.&nbsp; &nbsp;</p>


