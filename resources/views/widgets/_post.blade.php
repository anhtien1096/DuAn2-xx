<p>Tổng giám đốc VNPT Phạm Đức Long cho biết, sau khi Bộ TT&amp;TT yêu cầu các doanh nghiệp phải xử lý vấn nạn SIM rác, tin nhắn rác và thu hồi SIM đã kích hoạt sẵn trên kênh phân phối, VNPT đã nghiêm túc thực hiện chủ trương này. Ông Phạm Đức Long đưa ra dẫn chứng, nếu trước đây mỗi ngày mạng VinaPhone ghi nhận khoảng 100.000 tin nhắn rác thì đến nay chỉ còn 2.000 tin. Với việc đang quản lý hơn 30 triệu thuê bao Vinaphone, thì cứ 10.000 thuê bao có 1,5 tin nhắn rác.</p>

<p>"Từ tháng 2 đến nay đã có 30 giám đốc bán hàng bị xử phạt, trong đó 3 người bị điều chuyển công tác, 18 đơn vị bị hạ lương. Những doanh nghiệp ký hợp đồng ủy quyền không nghiêm túc sẽ hủy hợp đồng khóa tài khoản. VNPT thực hiện nghiêm túc thực hiện chủ trương của Chính phủ và Bộ TT&amp;TT, nhưng thực tế chưa hết được SIM rác. Hiện nay vẫn còn tồn đọng trên kênh phân phối những SIM đã kích hoạt” ông Phạm Đức Long nói.</p>

<p>Trả lời ICTnews về việc thực hiện cam kết thu hồi SIM đã kích hoạt trên kênh phân phối, ông Lương Mạnh Hoàng, Phó tổng giám đốc VNPT cho biết, ngay sau khi các nhà mạng ký cam kết, Tập đoàn VNPT đã chỉ đạo các đơn vị kinh doanh nghiêm túc thực hiện chỉ đạo của Bộ. Việc này cũng có quy trình. Thứ nhất là ngăn chặn các SIM mới kích hoạt sẵn, điều này hoàn toàn nằm trong tầm tay. Bước 2 VNPT tiến hành rà soát các SIM kích hoạt trước ngày chỉ đạo. Về cơ bản SIM kích hoạt 2016 đã khóa hết. Bản chất ngăn chặn SIM kích hoạt sẵn thực ra là để quản lý thông tin thuê bao.</p>



